#include <iostream>
using namespace std;
//Déclaration des variables
int nombre,x;

int main() //début
{
    cout << "entrer un nombre entier";
    for (x = 0; x < 1 < 10; x = nombre + x)
    {
        cout << nombre + x;
  }
}

